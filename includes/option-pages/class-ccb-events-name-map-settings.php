/**
 * Liquid Outreach Ccb Event Name Auto-Remapping Settings class.
 *
 * @since 0.21.2
 */
class LO_Ccb_Events_Name_Map_Settings extends LO_Base_Option_Page
{

    /**
     * Holds an instance of the object
     *
     * @var LO_Ccb_Events_Name_Map_Settings
     * @since 0.21.2
     */
    protected static $instance = null;

    /**
     * Option key, and option page slug
     *
     * @var string
     * @since 0.21.2
     */

    protected $key = 'liquid_outreach_ccb_events_name_map_settings';
    /**
     * Options page metabox id
     *
     * @var string
     * @since 0.21.2
     */
    protected $metabox_id = 'liquid_outreach_ccb_events_name_map_settings_metabox';

    /**
     * Options page meta prefix
     *
     * @var string
     * @since 0.21.2
     */
    protected $meta_prefix = 'lo_events_name_map_';

    /**
     * Options Page title
     *
     * @var string
     * @since 0.21.2
     */
    protected $title = '';

    /**
     * Options Page hook
     *
     * @var string
     * @since 0.21.2
     */
    protected $options_page = '';

    /**
     * Constructor
     *
     * @since 0.21.2
     */
    public function __construct()
    {
        // Set our title
        $this->title = __('Outreach Event Auto Renamer', 'liquid-outreach');

        $this->hooks();
    }

    /**
     * parent hook override
     * @since 0.21.2
     */
    public function hooks()
    {
        parent::hooks(); // TODO: Change the autogenerated stub
    }

    /**
     * Returns the running object
     *
     * @return LO_Ccb_Events_Name_Map_Settings
     * @since 0.21.2
     */
    public static function get_instance()
    {
        if (null === self::$instance) {
            self::$instance = new self();
            self::$instance->hooks();
        }

        return self::$instance;
    }

    /**
     * Add the options metabox to the array of metaboxes
     *
     * @since  0.21.2
     */
    function add_options_page_metabox()
    {
        wp_enqueue_style('lc-plugin', Liquid_Outreach::$url . 'assets/css/lc-plugin.css');

        // hook in our save notices
        add_action("cmb2_save_options-page_fields_{$this->metabox_id}",
            array($this, 'settings_notices'), 10, 2);

        $cmb = new_cmb2_box(array(
            'id' => $this->metabox_id,
            'hookup' => false,
            'cmb_styles' => false,
            'show_on' => array(
                // These are important, don't remove
                'key' => 'options-page',
                'value' => array($this->key,)
            ),
        ));

        // Set our CMB2 fields

        $name_mapping_id = $cmb->add_field(array(
            'name' => __('Name Mappings', 'liquid-outreach'),
            'id' => $this->meta_prefix . 'name_mapping',
            'type' => 'group',
            'options' => array(
                'group_title' => esc_html__('Mapping {#}', 'liquid-outreach'),
                'add_button' => esc_html__('Add Another Mapping', 'liquid-outreach'),
                'remove_button' => esc_html__('Remove Mapping', 'liquid-outreach'),
            ),
        ));

        $cmb->add_group_field($name_mapping_id, array(
            'name' => esc_html__('Title to Match', 'liquid-outreach'),
            'desc' => '',
            'id' => 'title',
            'type' => 'text',
        ));

        $cmb->add_group_field($name_mapping_id, array(
            'name' => esc_html__('Replacement Title', 'liquid-outreach'),
            'desc' => 'Matched title post will be renamed to this',
            'id' => 'replace_title',
            'type' => 'text',
        ));

    }

    /**
     * Public getter method for retrieving protected/private variables
     *
     * @since  0.21.2
     *
     * @param  string $field Field to retrieve
     * @return mixed         Field value or exception is thrown
     * @throws Exception
     */
    public function __get($field)
    {
        // Allowed fields to retrieve
        if (in_array($field, array('key', 'metabox_id', 'meta_prefix', 'title', 'options_page'), true)) {
            return $this->{$field};
        }

        throw new Exception('Invalid property: ' . $field);
    }

}